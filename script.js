/* getting object values, if you want to access values from an object, you can find it by its key */

let Country =  {
  name: "England",
  population: 55.98,
  language: "English",
  primaryWeather: "cold and wet"


};

console.log (Country.name);
console.log (Country.population);
console.log (Country.language); /*using dot notation to access the value from the object */